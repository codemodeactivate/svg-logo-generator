function isCSSColorName(str) {
    return validColors.includes(str);
}

function validateColorInput(input) {
    if (input.match(/^#([0-9A-Fa-f]{3}){1,2}$/)) {
      return true; // input is a valid hexadecimal color
    } else if (isCSSColorName(input)) {
      return true; // input is a valid CSS color name
    } else {
      return false; // input is not valid
    }
  }

